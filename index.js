// Faça um programa que receba a massa de um objeto = 10kg com aceleração de 10 m/s e calcule a força
// Formula da força = Massa x aceleração

const aceleracao = 10;
var massa = 10;
const forca = aceleracao * massa;

console.log(`Massa: ${massa}Kg`);
console.log(`Aceleração: ${aceleracao}m/s`);
console.log(`Força: (${massa}Kg * ${aceleracao}m/s) = ${forca}N`);
